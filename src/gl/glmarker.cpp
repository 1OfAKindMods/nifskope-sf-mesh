void GLMarker::drawMarker( Scene * scene, bool solid ) const
{
	auto	prog = scene->setupProgram( ( !solid ? "wireframe.prog" : "selection.prog" ), GL_TRIANGLES );
	if ( !prog )
		return;
	scene->renderer->bindShape( (unsigned int) nv, 0x03, size_t( nf ) * 3 * sizeof( unsigned short ), &verts, faces );
	scene->renderer->fn->glDrawElements( GL_TRIANGLES, GLsizei( nf ) * 3, GL_UNSIGNED_SHORT, (void *) 0 );
}


